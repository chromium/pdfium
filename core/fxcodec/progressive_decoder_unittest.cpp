      decoder->LoadImageInfo(source, FXCODEC_IMAGE_GIF, &attr, true);
  ASSERT_EQ(FXCODEC_STATUS::kFrameReady, status);

  ASSERT_EQ(4, decoder->GetWidth());
  ASSERT_EQ(2, decoder->GetHeight());

